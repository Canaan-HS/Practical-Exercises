"""
Todo    算數運算符
"""
a, b = 20, 10

print(a + b) # 相加

print(a - b) # 相減

print(a * b) # 相乘

print(a / b) # 相除 求 商, 有餘數

print(a // b) # 相除 求 商, 無餘數

print(a % b) # 相除 求 餘

print(a ** 2) # a 的 2 次方

