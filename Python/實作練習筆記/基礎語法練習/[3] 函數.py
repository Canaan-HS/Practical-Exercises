"""
Todo ~~~~~~~~~~~~~~~~~~~~~~~~~~~~~
*   函數外建立變數
"""
a = "一個變數"

def pr_a():
    print(a)

# 調用
pr_a()

"""
Todo ~~~~~~~~~~~~~~~~~~~~~~~~~~~~~
*   當函數外與內部有相同的變數
*   會被視為不同的變數
"""
a = "函數外的變數"

def pr1():
    a = "函數內的變數"
    print(a)
    
pr1()
print(a)

"""
Todo ~~~~~~~~~~~~~~~~~~~~~~~~~~~~~
*   全域變數 global
*   宣告為全域時, 在函數內將修改函數外的同名變數
"""
a = "函數外的變數"

def pr2():
    global a
    a = "函數內的變數"
    print(a)
    
pr2()
print(a)